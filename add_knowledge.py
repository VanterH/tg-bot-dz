def clear_knowledge_base():
    """Очистка базы знаний"""
    db = SessionLocal()
    try:
        count = db.query(KnowledgeBase).count()
        if count > 0:
            print(f"📚 Найдено {count} записей. Очищаем...")
            db.query(KnowledgeBase).delete()
            db.commit()
            print("✅ База знаний очищена")
    except Exception as e:
        print(f"❌ Ошибка: {e}")
    finally:
        db.close()


def add_extended_knowledge():
    """Добавление расширенных данных в базу знаний"""
    db = SessionLocal()
    
    added = 0
    for data in EXTENDED_KNOWLEDGE:
        existing = db.query(KnowledgeBase).filter_by(kb_id=data["kb_id"]).first()
        if not existing:
            kb = KnowledgeBase(**data)
            db.add(kb)
            added += 1
            print(f"✅ Добавлено: {data['kb_id']} - {data['products'][0]}")
    
    db.commit()
    db.close()
    
    print(f"\n📊 ИТОГО: Добавлено {added} записей в базу знаний")
    return added


def show_stats():
    """Показать статистику базы знаний"""
    db = SessionLocal()
    count = db.query(KnowledgeBase).count()
    print(f"\n📚 В базе знаний: {count} записей")
    
    # Показать список тем
    all_topics = set()
    for kb in db.query(KnowledgeBase).all():
        if kb.topics:
            for t in kb.topics:
                all_topics.add(t)
    print(f"📋 Доступные темы: {', '.join(sorted(all_topics))}")
    
    db.close()


